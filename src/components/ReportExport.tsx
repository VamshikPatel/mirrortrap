export default function ReportExport({ profile, result }: ReportExportProps) {
  const [copied, setCopied] = useState(false);
  const timeoutRef = useRef<ReturnType<typeof setTimeout> | null>(null);

  const handleCopy = useCallback(() => {
    const text = generateTextReport(profile, result);
    navigator.clipboard.writeText(text).then(() => {
      setCopied(true);
      if (timeoutRef.current) clearTimeout(timeoutRef.current);
      timeoutRef.current = setTimeout(() => setCopied(false), 2000);
    });
  }, [profile, result]);

  const handleDownload = useCallback(() => {
    const html = generateHTMLReport(profile, result);
    const blob = new Blob([html], { type: 'text/html' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `mirrortrap-report-${profile.name.toLowerCase().replace(/\s/g, '-')}-${Date.now()}.html`;
    a.click();
    URL.revokeObjectURL(url);
  }, [profile, result]);

  const handleShare = useCallback(async () => {
    if (navigator.share) {
      const text = generateTextReport(profile, result);
      try {
        await navigator.share({
          title: `MirrorTrap Report — ${profile.name}`,
          text: text.slice(0, 500) + '...',
        });
      } catch { /* user cancelled */ }
    }
  }, [profile, result]);

  return (
    <motion.div
      initial={{ opacity: 0, y: 20 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ delay: 0.8, duration: 0.5 }}
      className="glass-card"
      style={{ padding: 20, marginTop: 24 }}
    >
      <div style={{
        fontSize: 11, fontWeight: 700, letterSpacing: '0.08em', textTransform: 'uppercase',
        color: 'var(--text-muted)', marginBottom: 14, display: 'flex', alignItems: 'center', gap: 8,
      }}>
        <FileText size={14} style={{ color: 'var(--accent-purple)' }} />
        Export Report
      </div>

      <div style={{ display: 'flex', gap: 8, flexWrap: 'wrap' }}>
        <ExportBtn onClick={handleDownload} icon={<Download size={14} />} label="Download HTML" color="#60a5fa" />
        <ExportBtn onClick={handleCopy} icon={copied ? <Check size={14} /> : <Copy size={14} />} label={copied ? 'Copied!' : 'Copy Text'} color={copied ? '#22c55e' : '#a78bfa'} />
        {typeof navigator !== 'undefined' && 'share' in navigator && (
          <ExportBtn onClick={handleShare} icon={<Share2 size={14} />} label="Share" color="#f59e0b" />
        )}
      </div>
    </motion.div>
  );
}

function ExportBtn({ onClick, icon, label, color }: { onClick: () => void; icon: React.ReactNode; label: string; color: string }) {
  return (
    <button
      onClick={onClick}
      style={{
        display: 'flex', alignItems: 'center', gap: 6,
        padding: '8px 16px', fontSize: 12, fontWeight: 600,
        color, background: `${color}08`, border: `1px solid ${color}20`,
        borderRadius: 8, cursor: 'pointer', fontFamily: 'var(--font-sans)',
        transition: 'all 0.2s',
      }}
      onMouseEnter={(e) => { e.currentTarget.style.background = `${color}15`; e.currentTarget.style.borderColor = `${color}40`; }}
      onMouseLeave={(e) => { e.currentTarget.style.background = `${color}08`; e.currentTarget.style.borderColor = `${color}20`; }}
    >
      {icon} {label}
    </button>
  );
}
