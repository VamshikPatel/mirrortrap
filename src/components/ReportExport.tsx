import { useCallback, useRef } from 'react';
import { motion } from 'framer-motion';
import { Download, FileText, Share2, Copy, Check } from 'lucide-react';
import { useState } from 'react';
import type { AttackResult, Profile } from '../api';

/**
 * Report Export — Generate and download/share a security report
 * - Copy as plain text
 * - Download as HTML report
 * - Share via Web Share API
 */

interface ReportExportProps {
  profile: Profile;
  result: AttackResult;
}

function generateTextReport(profile: Profile, result: AttackResult): string {
  const lines = [
    '═══════════════════════════════════════',
    '  MIRRORTRAP — Security Exposure Report',
    '═══════════════════════════════════════',
    '',
    `Date: ${new Date().toLocaleString()}`,
    '',
    '── TARGET PROFILE ──',
    `Name: ${profile.name}`,
    `Role: ${profile.role}`,
    `Organization: ${profile.organization}`,
    `Recent Activity: ${profile.recentActivity}`,
    `Known Connection: ${profile.connection}`,
    '',
    '── EXPOSURE ASSESSMENT ──',
    `Score: ${result.exposure_score}/100`,
    `Verdict: ${result.overall_verdict}`,
    `Risk Level: ${result.exposure_score >= 70 ? 'HIGH' : result.exposure_score >= 40 ? 'MEDIUM' : 'LOW'}`,
    '',
    '── SIMULATED ATTACK ──',
    `Sender: ${result.sender_identity}`,
    `Channel: ${result.message_type}`,
    `Goal: ${result.attacker_goal}`,
    '',
    'Message:',
    result.attack_message,
    '',
    '── EXPLOITED VULNERABILITIES ──',
    ...( result.exploited_details?.map((d, i) =>
      `${i + 1}. [${d.trigger}] ${d.phrase}\n   Why: ${d.why_it_works}\n   Counter: ${d.counter}`
    ) || ['None detected']),
    '',
    '── RECOMMENDED ACTIONS ──',
    '1. Verify all unsolicited messages through official channels',
    '2. Never click links in unexpected messages',
    '3. Enable two-factor authentication',
    '4. Review and minimize your public digital footprint',
    '5. Report suspicious messages to your security team',
    '',
    '═══════════════════════════════════════',
    '  Generated by MirrorTrap',
    '  Educational use only — Not a real attack',
    '═══════════════════════════════════════',
  ];
  return lines.join('\n');
}

